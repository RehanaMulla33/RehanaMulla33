class PrePostInc{
	public static void main(String args[]){
		int a=10;
		int b=11;
		int c=12;
		
		
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		System.out.println("After pre increment addion of a, b, c are");
		System.out.println(++a+ ++b + ++c);
		System.out.println("After post inrement addion of a, b, c are");
		System.out.println(a++ + b++ +c++);
		System.out.println("after post increment the values of a b c are");
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		}
		}