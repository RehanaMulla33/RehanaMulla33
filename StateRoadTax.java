package com.xworkz.controlstatements;

public class StateRoadTax {

	public static void main(String[] args) {
		String state = "KA";

		if (state == "KA") {
			System.out.println("The road tax of karnataka is 200 Rs");
		} else if (state == "TS") {
			System.out.println("The road tax of Telangana is 500 Rs");
		} else if (state == "MH") {
			System.out.println("The road tax of Maharashtra is 400 Rs");
		} else if (state == "TN") {
			System.out.println("The road tax of tamilnadu is 600 Rs");
		} else if (state == "UP") {
			System.out.println("The road tax of Uttar pradesh is 800 Rs");

		}

	}

}
